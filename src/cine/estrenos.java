    private void jComboBox1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jComboBox1ActionPerformed
        try {
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/cine", "root", "");

            String sql = "SELECT Titulo FROM Pelicula WHERE Tipo = 'estreno'";

            PreparedStatement statement = con.prepareStatement(sql);

            // Ejecutar la consulta
            ResultSet result = statement.executeQuery();
            
            ArrayList<String> peliculas = new ArrayList<>();

            while (result.next()) {
                String nombrePelicula = result.getString("Titulo");
                peliculas.add(nombrePelicula);
            }
            con.close();

            //  JComboBox con la lista de nombres de pel√≠culas
            DefaultComboBoxModel model = new DefaultComboBoxModel(peliculas.toArray());
            jComboBox1.setModel(model);
        } catch (SQLException e) {
            e.printStackTrace();
        }
    
    }//GEN-LAST:event_jComboBox1ActionPerformed

