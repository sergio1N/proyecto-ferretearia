    private void jButton1jButtonGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1jButtonGuardarActionPerformed
        // TODO add your handling code here:
        int maxId = 0;
        try {
            String getMaxIdQuery = "SELECT MAX(IDPelicula) FROM pelicula";
            Statement stmt = cn.con.createStatement();
            ResultSet rs = stmt.executeQuery(getMaxIdQuery);

            if (rs.next()) {
                maxId = rs.getInt(1); // Obtén el valor máximo
            }
        } catch (SQLException ex) {
            ex.printStackTrace();
        }

        // Incrementa el valor máximo en uno para obtener el siguiente ID
        int nuevoId = maxId + 1;
        String titulo = jTextField1.getText();
        String genero = jTextField3.getText();
        int duracion = Integer.parseInt(jTextField2.getText());
        String tipo = "clasico";

        try {
            // Utiliza PreparedStatement para evitar problemas de seguridad y SQL injection
            String query = "INSERT INTO pelicula (IDPelicula,Titulo, Genero, Duracion, Tipo) VALUES (?, ?, ?, ?,?)";
            PreparedStatement stmt = cn.con.prepareStatement(query);
            stmt.setInt(1, nuevoId);
            stmt.setString(2, titulo);
            stmt.setString(3, genero);
            stmt.setInt(4, duracion);
            stmt.setString(5, tipo);

            int rowsInserted = stmt.executeUpdate();
            if (rowsInserted > 0) {
                JOptionPane.showMessageDialog(this, "Pelicula insertada con éxito");
            } else {
                JOptionPane.showMessageDialog(this, "No se pudo insertar la película");
            }
            dispose();
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, nuevoId + "Error al insertar la película: " + ex.getMessage());
        }
    }//GEN-LAST:event_jButton1jButtonGuardarActionPerformed

